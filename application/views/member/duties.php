		<br>
			<h2><u><center>DUTIES, RESPONSIBILITIES AND OBLIGATIONS OF A<BR>REGULAR MEMBER </center></u></h2>
		<br><br>
			A regular TELESCOOP member shall have the following duties, responsibilities and obligations:
		<br><br>
			<p>1. Patronize regularly the TELESCOOP products and services</p>
			<p>2. Pay his obligations and participate in the TELESCOOP capital build-up and savings mobilization</p>
			<p>3. Attend and participate in the deliberation during Representative Assembly meetings as a</p><p>&nbsp;&nbsp;&nbsp;&nbsp;Representative or Proponent of resolutions or amendments.</p>
			<p>4. Participate in membership education programs</p>
			<p>5. Observe and obey all lawful orders, decisions, rules and regulations adopted by the Board of</p><p> &nbsp;&nbsp;&nbsp;&nbsp;Directors and Representative Assembly.</p>
			<p>6. Promote the goals and objectives of TELESCOOP, the success of its business, the welfare of</p><p> &nbsp;&nbsp;&nbsp;&nbsp;its members and the cooperative movement in general.</p>
		<br><br>
		<h2><u><center>RIGHTS OF A REGULAR MEMBER</center></u></h2>
		<br><br>
			A regular “member in good standing” shall have the following rights:
		<br><br>
			<p>1. Participate and vote during Representative Assembly meetings as a Representative</p>
			<p>2. Seek any elective or appointive position subject to limitations imposed by the</p><p> &nbsp;&nbsp;&nbsp;&nbsp;TELESCOOP By-Laws or RA 9520.</p>
			<p>3. Avail himself of the products and services of TELESCOOP.</p>
			<p>4. Receive interest on his savings deposits and patronage refund commensurate with</p><p> &nbsp;&nbsp;&nbsp;&nbsp;his patronage or availment of loans</p>
			<p>5. Inspect and examine the TELESCOOP books of accounts, minutes books, share</p><p> &nbsp;&nbsp;&nbsp;&nbsp;capital register and other records during office hours.</p>
			<p>6. Exercise such other rights and privileges as may be provided by the</p><p> &nbsp;&nbsp;&nbsp;&nbsp;Representative Assembly.</p>
		<br><br>
		<h2><u><center>WHO IS A "MEMBER IN GOOD STANDING"?</center></u></h2>
		<br><br>
			A regular “member in good standing” is one who:
		<br><br>
			<p>1. Has paid the membership fee of ₱ 100.00 and the value of at least ten(10)</p><p> &nbsp;&nbsp;&nbsp;&nbsp;common shares or ₱ 100.00</p>
			<p>2. Is not delinquent in the payment of his share capital subscriptions,</p><p> &nbsp;&nbsp;&nbsp;&nbsp;obligations and other accounts payable to TELESCOOP.</p>
			<p>3. Has not violated any provision of the TELESCOOP By-Laws, the terms</p><p> &nbsp;&nbsp;&nbsp;&nbsp;and conditions of the subscription agreement and the decisions, guidelines,</p><p> &nbsp;&nbsp;&nbsp;&nbsp;rules and regulations promulgated by the Board of Directors and the Representative Assembly</p>
			<p>4. Is not a former or incumbent Officer of an entity engaged in a</p><p> &nbsp;&nbsp;&nbsp;&nbsp;business similar to the business of TELESCOOP</p>




















