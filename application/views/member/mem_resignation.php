			<h2><u><center>WHEN DOES TELESCOOP MEMBERSHIP END OR TERMINATE</center></u></h2>
		<br>
			<strong>I. Active Payroll Members</strong>
		<br><br>
			<p>Regular membership in TELESCOOP is co-terminus with the employment service of a member. In order that a member may be entitled to retirement benefits, he must have completed five (5) years continuous membership and must be both separated from employment service and membership with TELESCOOP.</p>
		<br><br>
			<strong>II. Non-Payroll Members</strong>
		<br><br>
			<p>Regular membership with TELESCOOP for Non-Payroll Members is terminated by death or voluntary resignation.</p>
		<br>
			<p>Generally, Regular membership with TELESCOOP ends or terminates:</p>
		<br>

			 	<p>1. Upon the death, insanity and permanent incapacity or judicial declaration by a competent court</p><p> &nbsp;&nbsp;&nbsp;&nbsp;of the insolvency of a member (Automatic Termination).</p>
			 	<p>2. Upon resignation or withdrawal of his membership subject to a 60-day prior written notice to</p><p> &nbsp;&nbsp;&nbsp;&nbsp;the Board of Directors (Voluntary Termination).</p>
			 	<p>3. Upon a majority vote of the Board of Directors terminating the membership of a member for</p><p> &nbsp;&nbsp;&nbsp;&nbsp;committing any of the following (Involuntary Termination):</p>
		<br>
				<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;a. Not patronizing the products, services and businesses of TELESCOOP for more than 12</p><p> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;months.</p>	 	
				<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;b. Fails and continuously fails to comply with his obligations</p>
				<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;c. Violating any provision of the By-Laws and rules of TELESCOOP</p>
				<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;d. Any act or omission injurious or prejudicial to the interest or welfare of TELESCOOP </p>

		
