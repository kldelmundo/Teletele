						<h2><u><center>PRIVILEGES OF A REGULAR MEMBER</center></u></h2>
					<br><br>
						Anchored on the universal cooperative principle “One Member, One Vote” relating to equal rights of members, a regular member enjoys the following rights and privileges:
					<br><br>
						<p>1. Avail loans subject to his credit standing or loaning capacity, evaluation by Credit and</p><p> &nbsp;&nbsp;&nbsp;&nbsp;Collection and approval by the Board of Directors.</p>
						<p>2. Subscribe and pay for his share capital subscription as part of continuous capital build-up.</p>
						<p>3. Deposit to and withdraw from his Savings Deposits Account.</p>
						<p>4. Seek elective position as a Representative, a Member of the Board of Directors, Audit</p><p> &nbsp;&nbsp;&nbsp;&nbsp;Committee and Election Committee subject to ELECOM rules and regulations.</p>
						<p>5. Attend the Annual Representative Assembly as an elected Representative or a Proponent</p><p> &nbsp;&nbsp;&nbsp;&nbsp;of Resolution/s or amendment/s.</p>
						<p>6. Attendance to cost-free trainings and seminars under the continuous TELESCOOP education</p><p> &nbsp;&nbsp;&nbsp;&nbsp;program</p>
					<br><br>
						<h2><u><center>BENEFITS OF A REGULAR MEMBER</center></u></h2>
					<br><br>
						A regular member is entitled to the following benefits:
					<br><br>
						<p>1. Annual payment of Interest on his Share Capital and Patronage Refund commensurate with</p><p> &nbsp;&nbsp;&nbsp;&nbsp;the frequency of his patronization or availment of loans.</p>
						<p>2. Monthly crediting of Interest on Savings Deposits based on lowest monthly balance.</p>
						<p>3. Membership Retirement Benefits conditioned upon five (5) years continuous</p><p> &nbsp;&nbsp;&nbsp;&nbsp;membership with TELESCOOP.</p>
						<p>4. Death Benefits of Php 200,000.00 payable to the heirs and beneficiaries of</p><p> &nbsp;&nbsp;&nbsp;&nbsp;a deceased Regular Member.</p>
						<p>5. Attendance to cost-free trainings and seminars under the TELESCOOP continuous education</p><p> &nbsp;&nbsp;&nbsp;&nbsp;program</p>


