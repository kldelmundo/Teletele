			<h2><u><center>BASIC REQUIREMENTS FOR TELESCOOP MEMBERSHIP</center></u></h2>
		<br>
			The following are qualified for TELESCOOP membership:
		<br><br>
			1.  A prospective member must be a Filipino citizen and at least eighteen (18) years old and not more &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;than forty five (45)
				years old and must be:
		<br><br>
			
			 	<p>a. A regular employee of PLDT Inc.</p>
			 	<p>b. A regular employee of the cooperatives organized and owned by employees of PLDT Inc.</p>
			 	<p>c. A regular employee of PLDT Subsidiaries and Affiliates and companies owned and controlled</p><p>&nbsp;&nbsp;&nbsp;&nbsp;by PLDT Inc.</p>
			 	<p>d. A regular employee of companies within PLDT premises and that PLDT Inc. owns at least 40% </p><p>&nbsp;&nbsp;&nbsp;&nbsp;of the Capital Stock of such companies.</p>
		
		<br><br>

			2. Immediate members of the family and dependents of regular members which are categorized as follows:

		<br><br>

			<p><div class="mtitle2">If a regular member is married:</div></p>
		<br>
			<p>1. Husband or Wife who is not more than fifty (50) years old.</p>
			<p>2. Dependent children who are single and at least 18 years old and not more than 21 years old.</p>
		<br><br>

			<p><div class="mtitle2">If a regular member is unmarried or single:</div></p>
		<br>
			<p>1. Father or Mother who is not more than 50 years old.</p>
		<br><br>
			3.  An applicant for TELESCOOP membership must attend a cost-free Pre-Membership Seminar conducted by the TELESCOOP EDCOM
		<br><br>			
			4.  An applicant for TELESCOOP membership shall file an application for membership for acceptance or denial by the Board of
				Directors within thirty (30) days from date of filing. The approval of new membership is normally done during Regular Board Meeting scheduled during the last Thursday of the month. An approved applicant for TELESCOOP membership has duties and responsibilities as well as rights and privileges, Among other duties and responsibilities, an approved applicant must pay membership fees, and other dues through payroll deduction or over-the-counter payment. 
